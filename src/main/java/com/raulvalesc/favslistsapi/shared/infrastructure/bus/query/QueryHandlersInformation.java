package com.raulvalesc.favslistsapi.shared.infrastructure.bus.query;

import com.raulvalesc.favslistsapi.shared.domain.bus.query.Query;
import com.raulvalesc.favslistsapi.shared.domain.bus.query.QueryHandler;
import com.raulvalesc.favslistsapi.shared.domain.bus.query.exception.QueryNotRegisteredException;
import com.raulvalesc.favslistsapi.shared.domain.injectable.Injectable;
import org.reflections.Reflections;

import java.lang.reflect.ParameterizedType;
import java.util.HashMap;
import java.util.Set;

@Injectable
public final class QueryHandlersInformation {
    private HashMap<Class<? extends Query>, Class<? extends QueryHandler>> indexedQueryHandlers;

    public QueryHandlersInformation() {
        Reflections reflections = new Reflections("com.raulvalesc");

        Set<Class<? extends QueryHandler>> classes = reflections.getSubTypesOf(QueryHandler.class);

        indexedQueryHandlers = formatHandlers(classes);
    }

    public Class<? extends QueryHandler> search(Class<? extends Query> queryClass) {
        Class<? extends QueryHandler> queryHandlerClass = indexedQueryHandlers.get(queryClass);

        if (null == queryHandlerClass) {
            throw new QueryNotRegisteredException(queryClass.getSimpleName());
        }

        return queryHandlerClass;
    }

    private HashMap<Class<? extends Query>, Class<? extends QueryHandler>> formatHandlers(Set<Class<? extends QueryHandler>> queryHandlers) {
        HashMap<Class<? extends Query>, Class<? extends QueryHandler>> handlers = new HashMap<>();

        for (Class<? extends QueryHandler> handler : queryHandlers) {
            ParameterizedType paramType = (ParameterizedType) handler.getGenericInterfaces()[0];

            Class<? extends Query> queryClass = (Class<? extends Query>) paramType.getActualTypeArguments()[0];

            handlers.put(queryClass, handler);
        }

        return handlers;
    }
}
